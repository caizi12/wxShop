package ngves.asiainfo.core.ware.model;

import java.io.Serializable;

/**
 * 用于保存期刊杂志名称属性
 * @author zhulin
 *
 */
public class MagBean implements Serializable{
    
    private static final long serialVersionUID = -5310431613337628345L;
    
    public MagBean(int year,int month){
        this.year = year;
        this.month = month;
    }
    //---期刊年份--
    private int year;
    //--期刊月份--
    private int month;
    public int getYear() {
        return year;
    }
    public void setYear(int year) {
        this.year = year;
    }
    public int getMonth() {
        return month;
    }
    public void setMonth(int month) {
        this.month = month;
    }

}
