package ngves.asiainfo.core.smsformwork.model;

import java.io.Serializable;

/**
 * 短信内容参数查询接口bean
 * @author yeqh
 * 2013-12-13
 */
public class SmsContentArgBean implements Serializable{

	private static final long serialVersionUID = -745223758099976889L;
	private String rejectReason;
	private String errorMsg;
	private int rowsCount;
	private String argCode;
	private String argName;
	
	
	public String getRejectReason() {
		return rejectReason;
	}
	public void setRejectReason(String rejectReason) {
		this.rejectReason = rejectReason;
	}
	public String getErrorMsg() {
		return errorMsg;
	}
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	public int getRowsCount() {
		return rowsCount;
	}
	public void setRowsCount(int rowsCount) {
		this.rowsCount = rowsCount;
	}
	public String getArgCode() {
		return argCode;
	}
	public void setArgCode(String argCode) {
		this.argCode = argCode;
	}
	public String getArgName() {
		return argName;
	}
	public void setArgName(String argName) {
		this.argName = argName;
	}
	
	

}
