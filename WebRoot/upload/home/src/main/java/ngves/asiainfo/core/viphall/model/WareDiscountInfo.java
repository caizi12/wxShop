package ngves.asiainfo.core.viphall.model;

import java.io.Serializable;

/**
 * 专题列表页面父礼品打折规则信息 父子礼品：对子礼品存在打折情况，那么父礼品打折，不考虑当前父礼品是上线还是下线。
 * 
 * @author shenfl
 * @version 1.0
 * 
 */
public class WareDiscountInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	// 统一积分数值
	private long wareIntegralValue;
	// 全球通当前积分数值
	private long wareGValue;
	// 动感地带当前M数值
	private long wareMValue;
	// 神州行当前积分值
	private long wareEValue;
	
	//混合支付的现金和积分值
	private long oriPayCash;
	private long oriPayInteger;
	private long curPayCash;
	private long curPayInteger;

	public long getWareIntegralValue() {
		return wareIntegralValue;
	}

	public void setWareIntegralValue(long wareIntegralValue) {
		this.wareIntegralValue = wareIntegralValue;
	}

	public long getWareGValue() {
		return wareGValue;
	}

	public void setWareGValue(long wareGValue) {
		this.wareGValue = wareGValue;
	}

	public long getWareMValue() {
		return wareMValue;
	}

	public void setWareMValue(long wareMValue) {
		this.wareMValue = wareMValue;
	}

	public long getWareEValue() {
		return wareEValue;
	}

	public void setWareEValue(long wareEValue) {
		this.wareEValue = wareEValue;
	}

	public long getOriPayCash() {
		return oriPayCash;
	}

	public void setOriPayCash(long oriPayCash) {
		this.oriPayCash = oriPayCash;
	}

	public long getOriPayInteger() {
		return oriPayInteger;
	}

	public void setOriPayInteger(long oriPayInteger) {
		this.oriPayInteger = oriPayInteger;
	}

	public long getCurPayCash() {
		return curPayCash;
	}

	public void setCurPayCash(long curPayCash) {
		this.curPayCash = curPayCash;
	}

	public long getCurPayInteger() {
		return curPayInteger;
	}

	public void setCurPayInteger(long curPayInteger) {
		this.curPayInteger = curPayInteger;
	}
	
}
