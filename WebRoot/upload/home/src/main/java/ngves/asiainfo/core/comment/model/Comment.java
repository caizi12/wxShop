package ngves.asiainfo.core.comment.model;

import java.io.Serializable;
import java.sql.Timestamp;

public class Comment implements Serializable{

    
	private static final long serialVersionUID = -5533331201263265873L;

	/**
	 * 礼品评论表对应实体
	 */
	
	//礼品原评论内容
    private String commentOriginal; 
    
    //评论时间
    private Timestamp commentDate;
    
    //评论ID
    private long commentId;
   
    //子礼品ID或普通礼品ID
    private long wareId;
    
    //子礼品名或普通礼品名
    private String wareName;
    
    //父礼品ID
    private long pWareId;

	//开始索引
    private int startIndex;
    
    // 结束索引
    private int endIndex;
    
	//子礼品或普通礼品CODE
    private String wareCode;
    
    //评论状态（0 无效 1有效）
    private String auditFlag;
    
    //审核时间
    private Timestamp auditTime;
    
    //评论人手机号
    private String mobilePhoneNo;
    
    //审核员工号
    private long auditStaffId;

    //礼品评级
    private long commentValue;
    
    private String bak01;
    
    private String bak02;

	public String getCommentOriginal() {
		return commentOriginal;
	}

	public void setCommentOriginal(String commentOriginal) {
		this.commentOriginal = commentOriginal;
	}

	public Timestamp getCommentDate() {
		return commentDate;
	}

	public void setCommentDate(Timestamp commentDate) {
		this.commentDate = commentDate;
	}

	public long getCommentId() {
		return commentId;
	}

	public void setCommentId(long commentId) {
		this.commentId = commentId;
	}

	public long getWareId() {
		return wareId;
	}

	public void setWareId(long wareId) {
		this.wareId = wareId;
	}

	public String getWareName() {
		return wareName;
	}

	public void setWareName(String wareName) {
		this.wareName = wareName;
	}

	

	public String getWareCode() {
		return wareCode;
	}

	public void setWareCode(String wareCode) {
		this.wareCode = wareCode;
	}

	public String getAuditFlag() {
		return auditFlag;
	}

	public void setAuditFlag(String auditFlag) {
		this.auditFlag = auditFlag;
	}

	public Timestamp getAuditTime() {
		return auditTime;
	}

	public void setAuditTime(Timestamp auditTime) {
		this.auditTime = auditTime;
	}

	public String getMobilePhoneNo() {
		return mobilePhoneNo;
	}

	public void setMobilePhoneNo(String mobilePhoneNo) {
		this.mobilePhoneNo = mobilePhoneNo;
	}

	public long getAuditStaffId() {
		return auditStaffId;
	}

	public void setAuditStaffId(long auditStaffId) {
		this.auditStaffId = auditStaffId;
	}

	public long getCommentValue() {
		return commentValue;
	}

	public void setCommentValue(long commentValue) {
		this.commentValue = commentValue;
	}

	public String getBak01() {
		return bak01;
	}

	public void setBak01(String bak01) {
		this.bak01 = bak01;
	}

	public String getBak02() {
		return bak02;
	}

	public void setBak02(String bak02) {
		this.bak02 = bak02;
	}

    public int getStartIndex() {
		return startIndex;
	}

	public void setStartIndex(int startIndex) {
		this.startIndex = startIndex;
	}

	public int getEndIndex() {
		return endIndex;
	}

	public void setEndIndex(int endIndex) {
		this.endIndex = endIndex;
	}

	public long getPWareId() {
    	
		return this.pWareId;
	}

	public void setPWareId(long pWareId) {
		
		this.pWareId = pWareId;
	}


    

}
